     */
    public function show($slug)
    {
        $tag = Tag::where('slug', $slug)->first();
        if ($tag) {
            $posts = $tag->posts()->orderBy('created_at', 'desc')->paginate(9);
            return view('frontend.tag', ['tag' => $tag, 'posts' => $posts]);
        } else
            return redirect()->back();
    }

